    public void limpiartabla(){
        try {
            for (int i=jtaddimpuestos.getRowCount()-1; i>=0;i--)
            {
            tablam.removeRow(i);
            }
        } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error limpiando tabla");
        }
        jtaddimpuestos.setModel(tablam);
    }    
    
    public void guardarimpuestos(){
        Impuestos nimpuesto = new Impuestos();
        
        nimpuesto.setNombre(txtnimpuesto.getText().toUpperCase());
        
//        if (txtformula.getText().isEmpty()) {
//            nimpuesto.setFormula("0");
//        }
//        else{
//            nimpuesto.setFormula(txtformula.getText().toUpperCase());
//        }
        
        try {
            Impuestos otimp = new Impuestos();
            otimp= (Impuestos) dao.findByWhereStatementoneobj(Impuestos.class, "nombre = '" + txtnimpuesto.getText().toUpperCase() + "'");
            if (otimp==null){
                dao.save(nimpuesto);
                limpiartabla();
                cargarjtable();
                centrardatos();
                limpiarpantalla();
                JOptionPane.showMessageDialog(this, "Guardado Completo");
            }
            else {
                JOptionPane.showMessageDialog(this, "El Impuesto ya existe");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hubo un error al guardar los datos, por favor intente mas tarde");
            e.printStackTrace();
        }
    }
    
    public void limpiarpantalla(){
        txtnimpuesto.setText("");
        txtformula.setText("");
    }
