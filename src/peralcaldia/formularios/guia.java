    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            Map<String, Object> params = new HashMap<String, Object>();
                String reporteLocation ="src/formularios/guiaconduccion.jrxml";
                params.put("nombre",jTextNombre.getText());
                params.put("motorista",jTextConductor.getText());
                params.put("placa",jTextPlaca.getText());
                params.put("color",jTextColor.getText());
                params.put("cantidad",jTextCantidad.getText());
                params.put("cartas",jTextCartas.getText());
                params.put("destino",jTextDestino.getText());
                
                JasperReport reporte=   JasperCompileManager.compileReport(reporteLocation);
                JasperPrint print = JasperFillManager.fillReport(reporte,params,new JREmptyDataSource());
                JasperViewer.viewReport(print,false);
        }catch(Exception e   ){
        System.out.print(e.getMessage());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

